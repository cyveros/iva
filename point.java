import java.awt.*;
import java.awt.event.*;
import java.awt.image.*;
import java.io.*;
import javax.imageio.*;
import java.math.*;

public class point {
	
	private int x = 0; 
	private int y = 0;
	
	public point (int x, int y){
		this.x = x;
		this.y = y;
	}

	public int get_x(){
		return x;
	}
	
	public int get_y(){
		return y;
	}

} 	



